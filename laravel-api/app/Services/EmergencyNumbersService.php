    /**
     * Génère les entrées CountryDirectory pour un pays.
     * Retourne jusqu'à 4 entrées (police, pompiers, médical, général).
     */
    public function generateForCountry(string $countryIso): array
    {
        $iso  = strtoupper($countryIso);
        $data = self::DATA[$iso] ?? null;
        if (!$data) return [];

        $countryName = WikidataService::COUNTRY_NAMES_FR[$iso] ?? $iso;
        $continent   = WikidataService::COUNTRY_CONTINENT[$iso] ?? 'autre';
        $slug        = $this->makeSlug($countryName);

        $entries = [];
        $services = [
            'general' => $data['g'],
            'police'  => $data['p'],
            'fire'    => $data['f'],
            'medical' => $data['m'],
        ];

        // Dédupliquer : ne pas créer de doublon si même numéro que le général
        $seen = [];
        foreach ($services as $type => $number) {
            $key = $number . ':' . $type;
            // Toujours inclure le général, et dédupliquer les autres si identiques
            if ($type !== 'general' && in_array($number, $seen)) continue;
            $seen[] = $number;

            $titleFr = self::SERVICE_LABELS[$type]['fr'] . ' — ' . $countryName;
            $translations = [];
            foreach (['en','es','ar','de','pt'] as $lang) {
                $label = self::SERVICE_LABELS[$type][$lang] ?? null;
                if ($label) $translations[$lang] = ['title' => $label . ' — ' . $countryName];
            }

            $entries[] = [
                'country_code'     => $iso,
                'country_name'     => $countryName,
                'country_slug'     => $slug,
                'continent'        => $continent,
                'nationality_code' => null,
                'nationality_name' => null,
                'category'         => 'urgences',
                'sub_category'     => $type,
                'title'            => $titleFr,
                'url'              => 'tel:' . $number,
                'domain'           => 'tel',
                'description'      => $this->buildDescription($services, $countryName),
                'language'         => 'fr',
                'translations'     => $translations,
                'address'          => null,
                'city'             => null,
                'phone'            => $number,
                'phone_emergency'  => $data['m'], // numéro médical = le plus critique
                'email'            => null,
                'opening_hours'    => '24h/24 — 7j/7',
                'latitude'         => null,
                'longitude'        => null,
                'emergency_number' => $data['g'], // numéro principal
                'trust_score'      => 100,
                'is_official'      => true,
                'is_active'        => true,
                'anchor_text'      => strtolower("urgences $type $countryName"),
                'rel_attribute'    => 'noopener',
            ];
        }

        return $entries;
    }

    /**
     * Génère les entrées pour tous les pays connus.
     */
    public function generateForAll(): array
    {
        $all = [];
        foreach (array_keys(self::DATA) as $iso) {
            $all = array_merge($all, $this->generateForCountry($iso));
        }
        return $all;
    }

    public function getSupportedIsoCodes(): array
    {
        return array_keys(self::DATA);
    }

    private function buildDescription(array $services, string $country): string
    {
        $parts = [];
        if ($services['g']) $parts[] = $services['g'] . ' (urgences)';
        if ($services['p'] && $services['p'] !== $services['g']) $parts[] = $services['p'] . ' (police)';
        if ($services['f'] && $services['f'] !== $services['g']) $parts[] = $services['f'] . ' (pompiers)';
        if ($services['m'] && $services['m'] !== $services['g']) $parts[] = $services['m'] . ' (médical)';
        return implode(' · ', $parts);
    }

    private function makeSlug(string $name): string
    {
        $map = ['é'=>'e','è'=>'e','ê'=>'e','à'=>'a','â'=>'a','î'=>'i','ô'=>'o','ù'=>'u','û'=>'u','ç'=>'c',"'"=>'-'];
        $name = strtolower(strtr($name, $map));
        $name = preg_replace('/[^a-z0-9\-]/', '-', $name);
        return trim(preg_replace('/-+/', '-', $name), '-');
    }
